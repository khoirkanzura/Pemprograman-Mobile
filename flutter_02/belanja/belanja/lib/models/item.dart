class Item {
  final String name;
  final int price;
  final String image;
  final double rating;
  final int stock;
  final String description;
  final String category;
  final String weight;
  final String condition;

  Item({
    required this.name,
    required this.price,
    required this.image,
    required this.rating,
    required this.stock,
    required this.description,
    required this.category,
    required this.weight,
    required this.condition,
  });
}
